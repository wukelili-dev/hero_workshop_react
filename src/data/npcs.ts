// ── 导出函数 ──

/** 按地图 ID 获取该地图中的 NPC 列表 */
export function getNpcsByMap(mapId: string): NpcDefinition[] {
  return NPCS.filter((n) => n.location === mapId);
}

/** 判断地图是否有 NPC */
export function hasNpcs(mapId: string): boolean {
  return NPCS.some((n) => n.location === mapId);
}

/** 随机选择一句问候语 */
export function randomGreeting(npc: NpcDefinition): string {
  return npc.greetings[Math.floor(Math.random() * npc.greetings.length)];
}

/** 随机选择一句对话 */
export function randomDialogue(npc: NpcDefinition): string {
  const pool = npc.chatDialogues ?? [];
  if (pool.length === 0) return npc.greetings[0] ?? '……';
  return pool[Math.floor(Math.random() * pool.length)];
}
